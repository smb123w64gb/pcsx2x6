#define ACATA_R_DATA                0x16000000  // Read/Write PIO data bytes
#define ACATA_R_FEATURE             0x16010000  // [W] Used to control command specific interface features.
#define ACATA_R_ERROR          ACATA_R_FEATURE  // [R] Used to retrieve any error generated by the last ATA command executed.	
#define ACATA_R_NSECTOR             0x16020000  // Number of sectors to read/write (0 is a special value).
#define ACATA_R_SECTOR              0x16030000  // This is CHS / LBA28 / LBA48 specific.
#define ACATA_R_LCYL                0x16040000  // Partial Disk Sector address.
#define ACATA_R_HCYL                0x16050000  // Partial Disk Sector address.
#define ACATA_R_SELECT              0x16060000  // Used to select a drive and/or head. Supports extra address/flag bits. 0x10 bit determines drive
#define ACATA_R_STATUS              0x16070000  // [R] Used to read the current status.
#define ACATA_R_COMMAND         ACATA_R_STATUS  // [W] Used to send ATA commands to the device.
#define ACATA_R_STATUS_ALT          0x16160000  // [R] A duplicate of the Status Register which does not affect interrupts.
#define ACATA_R_CONTROL     ACATA_R_STATUS_ALT  // [W] Used to reset the bus or enable/disable interrupts.


#define ACATA_RANGE              0x1600
#define ACATA_UNIT0              0x0  // 16 * (unitIndex != 0)
#define ACATA_UNIT1              0x10 // 16 * (unitIndex != 0)

#define FEAT_DMA 0x1 // the requested transfer will be done over DMA, not PIO
#define ACATA_ISDMA (ACATA::R_FEATURE & FEAT_DMA) // `*0x16010000 & 1` indicates DMA will be used


